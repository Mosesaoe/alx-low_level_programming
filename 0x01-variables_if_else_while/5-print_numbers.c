#include <stdio.h>
/**
* main - Entry point
*
* Return: Always 0 (Success)
*/
int main(void)
{
	int i; /* Statement Declaaration*/
	/*prints all single digit numbers of base 10 starting from 0*/
	for (i =0 ; i < 10; i++)
	{
		printf("%i", i); /*print i*/
	}
	
	printf("\n"); /*new line*/

	return (0);
}
